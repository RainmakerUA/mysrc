using System;

namespace RM.Win.BossKey.Win32
{
	[Flags]
	internal enum WS : long
	{
		OVERLAPPED = 0x00000000L,
		POPUP = 0x80000000L,
		CHILD = 0x40000000L,
		MINIMIZE = 0x20000000L,
		VISIBLE = 0x10000000L,
		DISABLED = 0x08000000L,
		CLIPSIBLINGS = 0x04000000L,
		CLIPCHILDREN = 0x02000000L,
		MAXIMIZE = 0x01000000L,
		BORDER = 0x00800000L,
		DLGFRAME = 0x00400000L,
		VSCROLL = 0x00200000L,
		HSCROLL = 0x00100000L,
		SYSMENU = 0x00080000L,
		THICKFRAME = 0x00040000L,
		GROUP = 0x00020000L,
		TABSTOP = 0x00010000L,

		MINIMIZEBOX = 0x00020000L,
		MAXIMIZEBOX = 0x00010000L,

		CAPTION = BORDER | DLGFRAME,
		POPUPWINDOW = POPUP
							| BORDER
							| SYSMENU,
		OVERLAPPEDWINDOW = OVERLAPPED
							| CAPTION
							| SYSMENU
							| THICKFRAME
							| MINIMIZEBOX
							| MAXIMIZEBOX
	}
}
